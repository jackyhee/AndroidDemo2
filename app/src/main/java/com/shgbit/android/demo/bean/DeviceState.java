package com.shgbit.android.demo.bean;

public class DeviceState {
    private String pid;
    private String status;

    public DeviceState() {
    }

    public String getPid() {
        return this.pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getStatus() {
        return this.status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
